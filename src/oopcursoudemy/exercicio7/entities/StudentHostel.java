package oopcursoudemy.exercicio7.entities;

public class StudentHostel {
    private String name;
    private String email;

    public StudentHostel(String name, String email) {
        this.name = name;
        this.email = email;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
